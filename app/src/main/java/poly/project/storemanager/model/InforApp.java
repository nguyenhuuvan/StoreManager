package poly.project.storemanager.model;

public class InforApp {
    private final String name;
    private final String des;

    public InforApp(String name, String des) {
        this.name = name;
        this.des = des;
    }

    public String getName() {
        return name;
    }


    public String getDes() {
        return des;
    }
}
